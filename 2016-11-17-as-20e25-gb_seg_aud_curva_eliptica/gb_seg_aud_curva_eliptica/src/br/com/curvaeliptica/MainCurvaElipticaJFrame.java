    private void vButtonCalcularSomaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vButtonCalcularSomaActionPerformed
        String vTmpA = this.vTextFieldA.getText();
        String vTmpB = this.vTextFieldB.getText();
        String vTmpP1X = this.vTxtP1X.getText();
        String vTmpP1Y = this.vTxtP1Y.getText();
        String vTmpP2X = this.vTxtP2X.getText();
        String vTmpP2Y = this.vTxtP2Y.getText();
        String vTmpP = null;

        if (this.vComboValorP.getSelectedItem() != null) {
            vTmpP = this.vComboValorP.getSelectedItem().toString();
        }

        if (Util.isNotNull(vTmpA, vTmpP, vTmpP1X, vTmpP1Y, vTmpP2X, vTmpP2Y)) {
            if (Util.isValidNumber(vTmpA, vTmpP, vTmpP1X, vTmpP1Y, vTmpP2X, vTmpP2Y)) {
                Ponto vP1 = new Ponto(vTmpP1X, vTmpP1Y);
                Ponto vP2 = new Ponto(vTmpP2X, vTmpP2Y);

                VG_Constantes._PASSO_A_PASSO_.doLimparPassoAPasso();
                CurvaEliptica vCurva = new CurvaEliptica(vTmpA, vTmpB, vTmpP);
                Ponto vP3 = vP1.doAdd_Aula(vP2, vCurva);

                if (VG_Constantes._PASSO_A_PASSO_.getStatus().equals(VG_Constantes._STATUS_SEM_ERRO)) {
                    this.vLabelResultOperacoes.setText("Resposta: " + vP3.toString());
                } else {
                    this.vLabelResultOperacoes.setText("Resposta: " + VG_Constantes._PASSO_A_PASSO_.getResultadoFinal());
                }

                this.vTxtAreaPassoAPasso.setText(VG_Constantes._PASSO_A_PASSO_.getPassoApasso());
                System.out.println("-># " + VG_Constantes._PASSO_A_PASSO_.getPassoApasso());

            } else {
                this.doLimparCampoOperacoes();
            }
        } else {
            this.doLimparCampoOperacoes();
        }

    }//GEN-LAST:event_vButtonCalcularSomaActionPerformed

