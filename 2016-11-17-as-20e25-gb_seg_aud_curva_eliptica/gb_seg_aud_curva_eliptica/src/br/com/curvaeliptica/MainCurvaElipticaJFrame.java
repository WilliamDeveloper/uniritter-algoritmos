    private void vButtonCalcularFormulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vButtonCalcularFormulaActionPerformed
        String tmpA = this.vTextFieldA.getText();
        String tmpB = this.vTextFieldB.getText();
        String tmpP = null;

        if (this.vComboValorP.getSelectedItem() != null) {
            tmpP = this.vComboValorP.getSelectedItem().toString();
        }

        if (Util.isNotNull(tmpA, tmpB, tmpP) && Util.isValidNumber(tmpA, tmpB, tmpP)) {
            CurvaEliptica c = new CurvaEliptica(tmpA, tmpB, tmpP);
            List<Ponto> vPontos = c.doGetListaPontosGrupoEliptico_Aula();

            StringBuffer vStrBuffer = new StringBuffer();
            vStrBuffer.append(VG_Constantes._PONTO_INFINITO);

            for (Ponto vPonto : vPontos) {
                vStrBuffer.append(", " + vPonto);
            }

            this.vTxtAreaGrupoEliptico.setText("E" + tmpP + "= { " + vStrBuffer + " }");

        } else {
            this.doLimparCampoGrupoEliptico();
        }
    }//GEN-LAST:event_vButtonCalcularFormulaActionPerformed

